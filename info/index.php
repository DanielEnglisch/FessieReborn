  <h1> Spielidee </h1>
  <p>
    “FESSIE räumt auf” ist ein Spiel ganz in der Tradition der Spielhallen-Art.
    Der Spieler schlüpft in die Rolle des Drachen FESSIE. Seine Aufgabe ist es,
    in vielen labyrinthartigen Levels diversen Müll aufzusammeln.
    Um durch die Level zu kommen muss FESSIE so manchen Gegner austricksen
    und verzwickte Hindernisse überwinden.
    Dabei stehen ihm Bomben und die Fähigkeit Feuer zu speien zur Verfügung.
  </p>
